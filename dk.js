function pwCheck ()  {
  if ($("#board_pw").val() == "") {
    alert("암호를 입력 하세요");
    $("#board_pw").focus();
    return false;
  }

  if ($("#board_pw2").val() == "") {
    alert("암호를 입력 하세요");
    $("#board_pw2").focus();
    return false;
  }

  if ($("#board_pw").val() != $("#pw2").val()) {
    alert("암호와 암호확인이 다릅니다");
    $("#pw2").val("");
    $("#board_pw").val("").focus();
    return false;
  }
}
