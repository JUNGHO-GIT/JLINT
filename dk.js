function memberCheck ()  {
  if ($("#id").val() == "") {
    alert("ID를 입력 하세요");
    $("#id").focus();
    return false;
  }

  if ($("#name").val() == "") {
    alert("이름을 입력 하세요");
    $("#name").focus();
    return false;
  }

  if ($("#email").val() == "") {
    alert("이메일을 입력 하세요");
    $("#email").focus();
    return false;
  }

  if ($("#tel").val() == "" || $("#tel2").val() == "" || $("#tel3").val() == "") {
    alert("전화번호를 정확히 입력 하세요");
    $("#tel").focus();
    return false;
  }

  return true;
}

