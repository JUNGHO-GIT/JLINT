function writeSave ()  {
  if ($("#board_writer").val() == "") {
    alert("이름을 입력 하세요 ");
    $("#board_writer").focus();
    return false;
  }
  if ($("#board_subject").val() == "") {
    alert("글제목을 입력 하세요 ");
    $("#board_subject").focus();
    return false;
  }
  if ($("#board_content").val() == "") {
    alert("글내용을 입력 하세요 ");
    $("#board_content").focus();
    return false;
  }
  if ($("#board_pw").val() == "") {
    alert("암호을 입력 하세요 ");
    $("#board_pw").focus();
    return false;
  }

  return true;
}

